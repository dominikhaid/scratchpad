public class Main {

  public static void main(String[] args) {
    System.out.println("Do loop");
    int i = 0;
    do {
      System.out.println(i);
      i++;
    } while (i < 5);

    System.out.println("While lopp");
    int b = 0;
    while (b < 5) {
      System.out.println(b);
      b++;
    }
  }
}
